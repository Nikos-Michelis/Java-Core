package main;

import java.lang.Math;

public class Main {

    public static void main(String[] args) {
        System.out.println("sin: "+Math.sin(Math.PI));
        System.out.println("e^x: "+Math.exp(1));
        System.out.println("log: "+Math.log(10));
        System.out.println("sqrt: "+Math.sqrt(9));
        System.out.println("floor: "+Math.floor(Math.PI));
        System.out.println("pow: "+Math.pow(Math.PI,2));
    }
}

